def main() -> None:
    db = SessionLocal()
    try:
        existing_titles = {t.title for t in db.query(BlogTopic).all()}
        added = 0
        for category, title, angle in TOPICS:
            if title in existing_titles:
                continue
            db.add(BlogTopic(category=category, title=title, angle=angle, status="unused"))
            added += 1
        db.commit()
        skipped = len(TOPICS) - added
        print(f"Seed complete: {added} added, {skipped} skipped (already existed). Total defined: {len(TOPICS)}")
    finally:
        db.close()


if __name__ == "__main__":
    main()
